# 테스트 케이스 개수를 입력받음
T = int(input())

# 최대 n이 10이므로 dp 배열은 미리 11까지 계산해 둠
dp = [0] * 11

# 기본값 설정
dp[1] = 1  # 1을 만드는 방법은 1가지 (1)
dp[2] = 2  # 2를 만드는 방법은 2가지 (1+1, 2)
dp[3] = 4  # 3을 만드는 방법은 4가지 (1+1+1, 1+2, 2+1, 3)

# 4부터 10까지 계산
for i in range(4, 11):
    dp[i] = dp[i-1] + dp[i-2] + dp[i-3]

# 각 테스트 케이스 처리
for _ in range(T):
    n = int(input())
    print(dp[n])
